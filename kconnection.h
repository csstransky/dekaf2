//::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
class KProxy
//::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
{

//------
public:
//------

	//-----------------------------------------------------------------------------
	KProxy() = default;
	KProxy(const KProxy&) = default;
	KProxy(KProxy&&) = default;
	KProxy& operator=(const KProxy&) = default;
	KProxy& operator=(KProxy&&) = default;
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	KProxy(const url::KDomain& domain,
	       const url::KPort& port,
	       KStringView svUser = KStringView{},
	       KStringView svPassword = KStringView{});
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	KProxy(KStringView svDomainAndPort,
	       KStringView svUser = KStringView{},
	       KStringView svPassword = KStringView{});
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	bool LoadFromEnv(KStringViewZ svEnvVar = "HTTP_PROXY");
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	void clear();
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	bool empty() const
	//-----------------------------------------------------------------------------
	{
		return Domain.empty();
	}

	url::KDomain Domain;
	url::KPort Port;
	KString User;
	KString Password;

}; // KProxy

//::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
class KConnection
//::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
{

//------
public:
//------

	//-----------------------------------------------------------------------------
	KConnection() = default;
	KConnection(const KConnection&) = delete;
	KConnection(KConnection&& other);
	KConnection& operator=(const KConnection&) = delete;
	KConnection& operator=(KConnection&& other);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	KConnection(KStream& Stream) noexcept
	//-----------------------------------------------------------------------------
	    : m_Stream(&Stream)
	    , m_bStreamIsNotOwned(true)
	{
	}

	//-----------------------------------------------------------------------------
	KConnection(KStream&& Stream) noexcept
	//-----------------------------------------------------------------------------
		: m_Stream(&Stream)
		, m_bStreamIsNotOwned(false)
	{
	}

	//-----------------------------------------------------------------------------
	virtual ~KConnection()
	//-----------------------------------------------------------------------------
	{
		if (m_Stream)
		{
			Disconnect();
		}
	}

	//-----------------------------------------------------------------------------
	virtual bool Good() const;
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	operator bool() const
	//-----------------------------------------------------------------------------
	{
		return Good();
	}

	//-----------------------------------------------------------------------------
	KConnection& operator=(KStream& Stream);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	KConnection& operator=(KStream&& Stream);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	KStream& Stream()
	//-----------------------------------------------------------------------------
	{
		return *StreamPtr();
	}

	//-----------------------------------------------------------------------------
	KStream* operator->()
	//-----------------------------------------------------------------------------
	{
		return StreamPtr();
	}

	//-----------------------------------------------------------------------------
	KStream& operator*()
	//-----------------------------------------------------------------------------
	{
		return *StreamPtr();
	}

	//-----------------------------------------------------------------------------
	virtual void Disconnect();
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	virtual bool SetTimeout(long iSeconds);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	virtual bool IsTLS() const;
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	virtual bool SetManualTLSHandshake(bool bYes = true);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	virtual bool StartManualTLSHandshake();
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	// this interface uses KURL instead of KTCPEndPoint to allow construction like "https://www.abc.de" - otherwise the protocol would be lost..
	static std::unique_ptr<KConnection> Create(const KURL& URL, bool bForceSSL = false, bool bVerifyCerts = false);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	// this interface uses KURL instead of KTCPEndPoint to allow construction like "https://www.abc.de" - otherwise the protocol would be lost..
	static std::unique_ptr<KConnection> Create(const KURL& URL, const KProxy& Proxy, bool bForceSSL = false, bool bVerifyCerts = false);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	virtual KString Error() const;
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	const KString& EndPoint() const
	//-----------------------------------------------------------------------------
	{
		return m_Endpoint;
	}

//------
protected:
//------

	//-----------------------------------------------------------------------------
	// returns true if connection is Good()
	bool setConnection(std::unique_ptr<KStream>&& Stream, KString EndPoint);
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	const KStream* StreamPtr() const
	//-----------------------------------------------------------------------------
	{
		return m_Stream.get();
	}

	//-----------------------------------------------------------------------------
	KStream* StreamPtr()
	//-----------------------------------------------------------------------------
	{
		return m_Stream.get();
	}

	KString m_Endpoint;

//------
private:
//------

	std::unique_ptr<KStream> m_Stream;
	bool m_bStreamIsNotOwned { false };

}; // KConnection


