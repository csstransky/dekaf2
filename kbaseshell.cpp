#include "klog.h"
#include "kbaseshell.h"

namespace dekaf2
{

//-----------------------------------------------------------------------------
KBaseShell::~KBaseShell() {}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
int KBaseShell::Close()
//-----------------------------------------------------------------------------
{
	KLog().debug(3, "KPIPE::Close");

	if (m_pipe)
	{
		m_iExitCode = pclose (m_pipe);
		m_pipe = nullptr;
	}
	else
	{
		return -1; //attempting to close a pipe that is not open
	}

	KLog().debug(3, "KPIPE::Close::Done:: Exit Code = {}", m_iExitCode);

	return (m_iExitCode);
} // Close

} // end of namespace dekaf2
